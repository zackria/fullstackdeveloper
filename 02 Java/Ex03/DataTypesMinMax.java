class DataTypesMinMax
{
    public static void main(String[] args) {

        System.out.println("Boolean Variable FALSE : "+ java.lang.Boolean.FALSE);
        System.out.println("Boolean Variable TRUE: "+ java.lang.Boolean.TRUE);

        System.out.println("Byte MIN_VALUE : "+ Byte.MIN_VALUE);
        System.out.println("Byte MIN_VALUE : "+ Byte.MAX_VALUE);
        System.out.println("Byte SIZE : "+ Byte.SIZE);

        //The number of bits used to represent a char value in unsigned binary form, constant 16.
        System.out.println("Character MIN_VALUE : "+ Character.MIN_VALUE);
        System.out.println("Character MIN_VALUE : "+ Character.MAX_VALUE);
        System.out.println("Character SIZE : "+ Character.SIZE);

        System.out.println("Short MIN_VALUE : "+ Short.MIN_VALUE);
        System.out.println("Short MIN_VALUE : "+ Short.MAX_VALUE);
        System.out.println("Short SIZE : "+ Short.SIZE);

        System.out.println("Long MIN_VALUE : "+ Long.MIN_VALUE);
        System.out.println("Long MIN_VALUE : "+ Long.MAX_VALUE);
        System.out.println("Long SIZE : "+ Long.SIZE);

        System.out.println("Integer MIN_VALUE : "+ Integer.MIN_VALUE);
        System.out.println("Integer MIN_VALUE : "+ Integer.MAX_VALUE);
        System.out.println("Integer SIZE : "+ Integer.SIZE);

        System.out.println("Float MIN_VALUE : "+ Float.MIN_VALUE);
        System.out.println("Float MIN_VALUE : "+ Float.MAX_VALUE);
        System.out.println("Float SIZE : "+ Float.SIZE);


        System.out.println("Double MIN_VALUE : "+ Double.MIN_VALUE);
        System.out.println("Double MIN_VALUE : "+ Double.MAX_VALUE);
        System.out.println("Double SIZE : "+ Double.SIZE);
    }
}
