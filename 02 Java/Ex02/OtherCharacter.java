public class OtherCharacter
{
    public static void main(String args[])
    {
      //Other Characters can be a-z lowercase
      //Other Characters can be A-Z UpperCase
      //Other Characters can contain $ and _
      //Other Characters can contain numbers 0 to 9
      String otherCharacter = "Valid otherCharacter";

      String o$therCharacter = "Valid o$therCharacter";

      String o_therCharacter = "Valid o_therCharacter";

      String o1therCharacter = "Valid o1therCharacter";

      //String o#therCharacter = "InValid o#therCharacter";

      System.out.println(otherCharacter);

      System.out.println(o$therCharacter);

      System.out.println(o_therCharacter);

      System.out.println(o1therCharacter);

      //System.out.println(o#therCharacter);

    }

}
